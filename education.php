                            À propos du Département de la protection des végétaux
                            Le domaine de la protection des plantes est considéré comme l'une des branches les plus importantes de l'agriculture et ce domaine comprend trois branches, qui sont les sciences des insectes, les pesticides et les animaux agricoles. La branche entomologie s'occupe de l'étude des insectes ravageurs qui affectent les vergers avec ses différentes branches (fruits - légumes - décorations), ainsi que des insectes qui affectent les grandes cultures, en plus de la biologie, de la physiologie, de la morphologie et de l'environnement des différents insectes. Il traite également de l'étude des insectes d'importance médicinale et vétérinaire ainsi que des insectes qui infectent les céréales stockées et les arbres ligneux. La section accorde une importance particulière au domaine de la lutte biologique et au domaine des abeilles. Le pesticide est une branche du domaine de la protection des plantes concernée par la lutte contre les ravageurs agricoles (insectes, acariens, nématodes, rongeurs, champignons, bactéries, mauvaises herbes et escargots).
                            Méthodes agricoles

                            Les méthodes agricoles sont considérées comme un moyen important de lutter contre les maladies des plantes et visent à exterminer les parasites phytopathogènes dans les conditions de terrain. Les méthodes agricoles sont appliquées de nombreuses manières, notamment:

                            1- Suivre un cycle agricole approprié:

                            L'un des facteurs les plus importants appropriés pour l'augmentation et la propagation de la maladie dans cette région est la culture continue de toute culture dans une zone spécifique, car la concentration du parasite dans cette région augmente d'année en année jusqu'à ce qu'elle devienne très nocive pour la plante. Et prendre en compte dans la conception du cycle agricole que les cultures successives ne sont pas sensibles aux mêmes maladies, car cette méthode permet de limiter la propagation d'agents pathogènes qui ne peuvent pas vivre longtemps en l'absence de leur famille.

                            2- Choisir le terrain adapté à la culture:

                            Là où l'agriculture est évitée sur des terres mal drainées, faibles, alcalines ou salées, et où la culture n'est pas appropriée pour certaines cultures où elle n'existe pas, et comme cela se produit chaque année en Égypte dans le cas de la sélection de terres exemptes de cause de pourriture brune des pommes de terre.

                            3- Prendre les précautions nécessaires lors de la collecte, du transport et du stockage de la récolte:

                            L'une des précautions les plus importantes est d'éviter de provoquer des coupures ou des rayures dans les fruits car elle facilite l'entrée de parasites pathogènes.


                            4- Suivez les méthodes et dates de culture appropriées:

                            Là où par cela, il est possible de réduire les risques de blessures à la plante ou de travailler pour lui échapper des blessures et de nombreux exemples de cela, la plantation de la surface ou de la surface du blé réduit son affliction avec les racines des racines, la culture par la méthode de la raquette et du sable et du côté tribal de la plume permet aux plants de coton d'échapper aux blessures causées par la diphtérie du coton.

                            5- Modération de l'irrigation et organisation en fonction des besoins des cultures:

                            Parce que plus d'irrigation que nécessaire aux plantes affaiblit le système racinaire des plantes et les expose aux maladies, et une humidité élevée du sol est appropriée pour la croissance de nombreux agents pathogènes des plantes.

                            6- Fertilisation appropriée en termes de type et de quantité d'engrais:

                            La fertilisation avec des engrais azotés augmente la croissance végétative et réduit l'épaisseur de la couche cotylique dans la plante, ce qui rend les plantes plus sensibles aux maladies. La fertilisation au potassium et au phosphore rend les plantes plus résistantes aux maladies.

                            7- Enlever, brûler et essuyer les mauvaises herbes:

                            Là où les mauvaises herbes abritent de nombreux agents pathogènes et sont des familles mutuelles pour elles, car elles concurrencent la culture dans sa nourriture et travaillent à l'affaiblir et à la contracter avec diverses maladies.

                            8- Suppression du fournisseur et du parasite:

                            Le processus d'élimination de la famille infectée et du parasite en même temps est considéré comme une méthode agricole importante pour se débarrasser efficacement de la maladie et cette méthode a été utilisée avec succès dans la lutte contre l'ulcération des agrumes et cette méthode est également suivie avec certaines maladies virales telles que la maladie qui alimente le sommet des bananiers où les plantes infectées sont déracinées et brûlées pour se débarrasser de la maladie .

                            9- Suivez les processus agricoles appropriés:

                            Le binage à intervalles réguliers et une bonne culture en profondeur conduisent à l'élimination des mauvaises herbes, qui abritent de nombreux parasites pathogènes. Le labour en profondeur permet de ventiler le sol et de l'exposer au soleil, ce qui provoque la mort de certains parasites.

                            10- Bain de soleil:

                            Le sol est recouvert de tranches de plastique polyéthylène pendant deux semaines ou plus, exposant le sol à une forte lumière solaire avec un effet néfaste sur les micro-organismes du sol et provoquant sa mort en raison de la température du sol dépassant 50 degrés Celsius. Cette méthode est utilisée pour lutter contre la maladie du flétrissement des tomates.

                            11- Changer le pH du sol:

                            La modification du pH du sol est une méthode agricole importante pour tuer ou empêcher l'activité de certains agents pathogènes. Cette méthode a été utilisée avec succès pour résister à la gale normale de la pomme de terre, où du soufre agricole a été ajouté au sol contaminé par la maladie malade, ce qui a conduit à réduire l'acidité du sol à un degré qui ne permet pas au patient malade de croître et d'être actif. Par conséquent, la maladie a été résistée.

